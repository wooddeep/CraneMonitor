public class ShowData {

    private float showHAngle = 1.0f;
    private float shadowRange = 1.0f;
    private float coordX = 100f;
    private float coordY = 100f;


    public float getShowHAngle() {
        return showHAngle;
    }

    public void setShowHAngle(float showHAngle) {
        this.showHAngle = showHAngle;
    }

    public float getShadowRange() {
        return shadowRange;
    }

    public void setShadowRange(float shadowRange) {
        this.shadowRange = shadowRange;
    }

    public float getCoordX() {
        return coordX;
    }

    public void setCoordX(float coordX) {
        this.coordX = coordX;
    }

    public float getCoordY() {
        return coordY;
    }

    public void setCoordY(float coordY) {
        this.coordY = coordY;
    }
}
